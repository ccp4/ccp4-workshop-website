<h2>The CCP4-BCA Summer School returns to the North!</h2> 
<p>It is a great pleasure to announce the return of the CCP4-BCA Summer School on 
      macromolecular crystallography and associated techniques. Running for the first 
      time since the COVID-19 pandemic, the school aims to bring together early-career
      scientists from the UK, Europe and beyond and train them in the latest advances
      in structural biology, with a primary focus on macromolecular crystallography.</p>

<p>All aspects of structure solution will be covered during the
workshop, from data collection through to phasing, refinement,
validation and deposition.</p>
<ul>

  <li> Lectures and tutorials will be delivered by experts in the field, sometimes even software authors themselves!
  <li> An exciting social programme will showcase some of the best the historical City of York can offer - and in summer.
  <li> You will be able to work alongside world-leading scientists and methods developers on your own projects.

</ul>
<p>The school will be based at <b>The University of York's Campus West</b>, with lectures in the main Biology building, workshops 
  in a PC room in James College and accommodation in Vanbrugh College.</p>
<p>More details and directions: <a title="Location information" href="location.php"><b>Location</b></a>.</p> 

<p>Spaces are limited. The applicant will have to demonstrate the need for this sort of specialist training, and be prepared 
   to submit the <b>e-mail address of a supervisor</b> who will write a <b>letter of recommendation</b> in support of their application.</p>

<p>We look forward to welcoming you in York this summer!</p>
<p>The organisers,<br>
Johan Turkenburg, Jon Agirre and Paul Bond.</p>

